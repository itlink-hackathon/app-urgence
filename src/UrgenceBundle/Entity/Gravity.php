<?php

namespace UrgenceBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Gravity
 *
 * @ORM\Table(name="gravity")
 * @ORM\Entity(repositoryClass="UrgenceBundle\Repository\GravityRepository")
 */
class Gravity
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="text",lenght=50)
     */
    private $name;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Gravity
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }
}
